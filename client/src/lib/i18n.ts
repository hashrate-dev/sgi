export function t(lang: string, key: string): string {
  const L = (lang in STRINGS ? lang : "es") as MarketplaceLang;
  return STRINGS[L]?.[key] ?? STRINGS.es[key] ?? key;
}

export function tf(lang: string, key: string, vars: Record<string, string> = {}): string {
  let s = t(lang, key);
  for (const [k, v] of Object.entries(vars)) {
    s = s.split(`{{${k}}}`).join(v);
  }
  return s;
}

export function mailtoHref(lang: string, email: string, subjectKey: string): string {
  const subject = encodeURIComponent(t(lang, subjectKey));
  return `mailto:${email}?subject=${subject}`;
}

/** Locale para números / fechas en marketplace */
export function marketplaceLocale(lang: string): string {
  return lang === "en" ? "en-US" : "es-PY";
}
